void main() {
  int a = 10;
  int b = 5;

  print(a == b); // false (১০ কি ৫ এর সমান? না)
  print(a != b); // true  (১০ কি ৫ এর সমান নয়? হ্যাঁ)
  print(a > b); // true  (১০ কি ৫ এর চেয়ে বড়? হ্যাঁ)
  print(a < b); // false (১০ কি ৫ এর চেয়ে ছোট? না)
  print(a >= b); // true  (১০ কি ৫ এর চেয়ে বড় বা সমান? হ্যাঁ)
  print(a <= b); // false (১০ কি ৫ এর চেয়ে ছোট বা সমান? না)
}
